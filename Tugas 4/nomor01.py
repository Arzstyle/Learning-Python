# Tuliskan program yang meminta input dua bilangan dan menampilkan bilangan yang lebih besar.

bilangan_1 = int(input("Masukan Bilangan 1 : "))
bilangan_2 = int(input("Masukan Bilangan 2 : "))

if bilangan_1 > bilangan_2:
    print(f"Bilangan 1 yaitu : {bilangan_1} lebih besar dari Bilangan 2 yaitu : {bilangan_2}")
else:
    print(f"Bilangan 2 yaitu : {bilangan_2} lebih besar dari Bilangan 1 yaitu : {bilangan_1}")