Reamur = float(input (" Masukan Suhu : " ))

Celcius = 0.0
Fahrenheit = 0.0
Kelvin = 0.0

rumusCelcius = 5/4 * Reamur
rumusFahrenheit = 9/4 * Reamur + 32
rumusKelvin = 5/4 * Reamur + 273.15

print("Suhu Reamur %2.f -> Celcius : %.2f" %(Reamur, rumusCelcius))
print("Suhu Reamur %2.f -> Fahrenheit : %.2f" %(Reamur, rumusFahrenheit))
print("Suhu Reamur %2.f -> Kelvin : %.2f" %(Reamur, rumusKelvin))

